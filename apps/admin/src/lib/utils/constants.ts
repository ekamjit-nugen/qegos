/** Current and recent financial years */
export function getFinancialYears(): string[] {
  const currentYear = new Date().getFullYear();
  const years: string[] = [];
  for (let y = currentYear + 1; y >= currentYear - 4; y--) {
    years.push(`${y - 1}-${y}`);
  }
  return years;
}

